import Foundation
import UIKit

extension UIColor {
    
    class func greenAppColor() -> UIColor {
        return UIColor(red: 25/255, green: 141/255, blue: 21/255, alpha: 1.00)
    }
    
    class func yellowAppColor() -> UIColor {
        return UIColor(red: 235/255, green: 255/255, blue: 99/255, alpha: 1.00)
    }
}
